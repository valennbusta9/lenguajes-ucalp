# Definimos una frase de ejemplo
frase = "Programación"

# Obtenemos las 3 primeras letras (del índice 0 al 2)
tres_primeras = frase[:3]

# Obtenemos las 3 últimas letras (desde el índice -3 hasta el final)
tres_ultimas = frase[-3:]

# Mostramos los resultados
print(f"Frase original: {frase}")
print(f"Las 3 primeras letras son: '{tres_primeras}'")
print(f"Las 3 últimas letras son: '{tres_ultimas}'")